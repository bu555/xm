        <dl>
            <dt>ENFP:</dt>
        　　<dd>1.充满热忱、活力充沛、聪明的、富想象力的，视生命充满机会但期能得自他人肯定与支持。</dd>
        　　<dd>2.几乎能达成所有有兴趣的事。</dd>
        　　<dd>3.对难题很快就有对策并能对有困难的人施予援手。</dd>
        　　<dd>4.依赖能改善的能力而无须预作规划准备。</dd>
        　　<dd>5.为达目的常能找出强制自己为之的理由。</dd>
        　　<dd>6.即兴执行者。</dd>
        </dl>
        <dl>
            <dt>INFP:</dt>
        　　<dd>1安静观察者，具理想性与对其价值观及重要之人具忠诚心。</dd>
        　　<dd>2.希外在生活形态与内在价值观相吻合。</dd>
        　　<dd>3.具好奇心且很快能看出机会所在。常担负开发创意的触媒者。</dd>
        　　<dd>4.除非价值观受侵犯，行事会具弹性、适应力高且承受力强。</dd>
        　　<dd>5.具想了解及发展他人潜能的企图。想作太多且作事全神贯注。</dd>
        　　<dd>6.对所处境遇及拥有不太在意。</dd>
        　　<dd>7.具适应力、有弹性除非价值观受到威胁。</dd>
        </dl>
        <dl>
            <dt>ENFJ:</dt>
            <dd>1.热忱、易感应及负责任的，具能鼓励他人的领导风格。</dd>
        　　<dd>2.对别人所想或希求会表达真正关切且切实用心去处理。</dd>
        　　<dd>3.能怡然且技巧性地带领团体讨论或演示文稿提案。</dd>
        　　<dd>4.爱交际、受欢迎及富同情心。</dd>
        　　<dd>5.对称许及批评很在意。</dd>
        　　<dd>6.喜欢带引别人且能使别人或团体发挥潜能。</dd>
        </dl>
