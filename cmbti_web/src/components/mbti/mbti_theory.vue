        　　<dd>2.喜于避开争论，不对他人强加已见或价值观。</dd>
        　　<dd>3.无意于领导却常是忠诚的追随者。</dd>
        　　<dd>4.办事不急躁，安于现状无意于以过度的急切或努力破坏现况，且非成果导向。</dd>
        　　<dd>5.喜欢有自有的空间及照自订的时程办事。</dd>
