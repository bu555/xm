        　　<dd>3.倾向于喜好技术事务及运动，交结同好友人。</dd>
        　　<dd>4.具适应性、容忍度、务实性；投注心力于会很快具　成效工作。</dd>
        　　<dd>5.不喜欢冗长概念的解释及理论。</dd>
