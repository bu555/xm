        　　<dd>1.因为坚忍、创意及必须达成的意图而能成功。</dd>
        　　<dd>2.会在工作中投注最大的努力。</dd>
        　　<dd>3.默默强力的、诚挚的及用心的关切他人。</dd>
        　　<dd>4.因坚守原则而受敬重。</dd>
        　　<dd>5.提出造福大众利益的明确远景而为人所尊敬与追随。</dd>
