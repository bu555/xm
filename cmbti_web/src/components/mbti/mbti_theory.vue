        <h2 class=''>MBTI <span>简述</span></h2>
        <p>MBTI的全名是Myers-Briggs Type Indicator。它是一种迫选型、自我报告式的性格评估理论模型，用以衡量和描述人们在获取信息、作出决策、对待生活等方面的心理活动规律和性格类型。</p>
        <p>MBTI人格理论的基础是著名心理学家卡尔·荣格先生关于心理类型的划分，后由美国的心理学家Katharine Cook Briggs 与Isabel Briggs Myers研究并加以发展。</p>
        <p>Katherine Cook Briggs (1875-1968) 和她的心理学家女儿Isabel Briggs Myers根据瑞士著名的心理分析学家Carl G. Jung (荣格)的心理类型理论和她们对于人类性格差异的长期观察和研究而著成。经过了长达50多年的研究和发展，MBTI已经成为了当今全球最为著名和权威的性格测试。</p>
        <p>这种理论可以帮助解释为什么不同的人对不同的事物感兴趣、善长不同的工作、并且有时不能互相理解。这个工具已经在世界上运用了将近30年的时间，夫妻利用它增进融洽、老师学生利用它提高学习、授课效率，青年人利用它选择职业，组织利用它改善人际关系、团队沟通、组织建设、组织诊断等多个方面。在世界五百强中，有80%的企业有MBTI的应用经验。</p>
        <p>MBTI主要应用于职业发展、职业咨询、团队建议、婚姻教育等方面，是目前国际上应用较广的人才甄别工具。MBTI也是目前国际最为流行的职业人格评估工具。</p>
