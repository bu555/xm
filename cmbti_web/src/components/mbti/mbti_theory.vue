        <h5>SJ型（ESTJ ISTJ ESFJ ISFJ）：忠诚的监护人</h5>
        <p>具有SJ偏好的人他们的共性是有很强的责任心与事业心，他们忠诚、按时完成任务，推崇安全、礼议、规则和服从，他们被一种服务于社会需要的强烈动机所驱使。他们坚定、尊重权威、等级制度，持保守的价值观。他们充当着保护者、管理员、稳压器、监护人的角色。大约有50%左右SJ偏爱的人为政府部门及军事部门的职务所吸引，并且显现出卓越成就。</p>
        <dl>
            <dt>ESTJ:</dt>
            <dd>1.务实、真实、事实倾向，具企业或技术天份。</dd>
            <dd>2.不喜欢抽象理论；最喜欢学习可立即运用事理。</dd>
        　　<dd>3.喜好组织与管理活动且专注以最有效率方式行事以达致成效。</dd>
        　　<dd>4.具决断力、关注细节且很快作出决策—优秀行政者。</dd>
        　　<dd>5.会忽略他人感受。</dd>
        　　<dd>6.喜作领导者或企业主管。</dd>
        </dl>
        <dl>
            <dt>ISTJ:</dt>
            <dd>1.严肃、安静、藉由集中心 志与全力投入、及可被信赖获致成功。</dd>
        　　<dd>2.行事务实、有序、实际 、 逻辑、真实及可信赖</dd>
        　　<dd>3.十分留意且乐于任何事（工作、居家、生活均有良好组织及有序。</dd>
        　　<dd>4.负责任。</dd>
        　　<dd>5.照设定成效来作出决策且不畏阻挠与闲言会坚定为之。</dd>
        　　<dd>6.重视传统与忠诚。</dd>
        　　<dd>7.传统性的思考者或经理。</dd>
        </dl>
        <dl>
            <dt>ESFJ:</dt>
        　　<dd>1.诚挚、爱说话、合作性高、受　欢迎、光明正大的、天生的合作者及活跃的组织成员。</dd>
        　　<dd>2.重和谐且长于创造和谐。</dd>
        　　<dd>3.常作对他人有益事务。</dd>
        　　<dd>4.给予鼓励及称许会有更佳工作成效。</dd>
        　　<dd>5.最有兴趣于会直接及有形影响人们生活的事务。</dd>
        　　<dd>6.喜欢与他人共事去精确且准时地完成工作。</dd>
        </dl>
        <dl>
            <dt>ISFJ:</dt>
        　　<dd>1.安静、和善、负责任、极具同情心。</dd>
        　　<dd>2.行事尽责投入。</dd>
        　　<dd>3.安定性高，常居项目工作或团体之安定力量。</dd>
        　　<dd>4.愿投入、吃苦及力求精确。</dd>
        　　<dd>5.兴趣通常不在于科技方面。对细节事务有耐心。</dd>
        　　<dd>6.忠诚、考虑周到、知性且会关切他人感受。</dd>
        　　<dd>7.致力于创构有序及和谐的工作与家庭环境。</dd>
