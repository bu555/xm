        <h2 class=''>MBTI <span>中的十六种类型</span></h2>
        <dl>
            <dt>四个维度，两两组合，共有十六种类型。以各个维度的字母表示类型，如下：</dt>
            <dd>SJ型（传统主义者）：ESTJ ISTJ ESFJ ISFJ</dd>
            <dd>SP型（经验主义者）：ESTP ISTP ESFP ISFP</dd>
            <dd>NF型（理想主义者）：ENFP INFP ENFJ INFJ</dd>
            <dd>NT型（概念主义者）：ENTP INTP ENTJ INTJ</dd>
        </dl>
        <p class="last">每种类型的四个维度在每个人身上会有不同的比重，不同的比重会导致不同的表现，关键在于各个维度上的人均指数和相对指数的大小。</p>
