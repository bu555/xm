        <h2 class=''>MBTI <span>人格理论的指标</span></h2>
        <dl>
            <dt>MBTI人格共有四个维度，每个维度有两个方向，共计八个方面。分别是：</dt>
            <dd>精力支配：外向 E --- 内向 I</dd>
            <dd>认识世界：感觉 S --- 直觉 N</dd>
            <dd>判断事物：思维 T --- 情感 F</dd>
            <dd>生活态度：判断 J --- 知觉 P</dd>
            <dd class="last">其中两两组合，可以组合成16种人格类型。</dd>
        </dl>
        <p>比如：我们与世界的相互作用可分为外向（E）和内向（I）；我们自然留意的信息类型有感觉（S）和直觉（N）之分；我们做决定的依据有有思考（T）和情感（F）之别；我们习惯的做事方式又分为判断（J）和知觉（P）</p>

        <h5>外向（E）和内向（I）</h5>
        <dl>
            <dt>外向（E）： </dt>
            <dd>从人际交往中获得能量</dd>
            <dd>喜欢外出</dd>
            <dd>表情丰富，外露</dd>
            <dd>喜欢交互作用，合群</dd>
            <dd>喜行动、多样性（不能长期坚持）</dd>
            <dd>不怕打扰，喜自由沟通</dd>
            <dd>讲，然后想；易冲动、易后悔、易受他人影响</dd>
        </dl>
        <dl>
            <dt>内向（I）： </dt>
            <dd>从自我独处中获得能量</dd>
            <dd>喜静、多思、冥想（离群、与外界相互误解）</dd>
            <dd>谨慎、不露表情</dd>
            <dd>社会行为的反射性（会失去机会）</dd>
            <dd>独立、负责、细致、周到、不蛮干</dd>
            <dd>不怕长时间做事、勤奋；怕打扰</dd>
            <dd>先想然后讲</dd>
        </dl>
        <h5>感觉（S）和直觉（N）</h5>
        <dl>
            <dt>感觉（S）:</dt>
            <dd>通过五官感受世界、注重真实的存在、实际。</dd>
            <dd>用已经有的技能解决问题</dd>
            <dd>喜具体明确的、可量化的</dd>
            <dd>重细节（少全面性）</dd>
            <dd>脚踏实地</dd>
            <dd>做事有可能的结果、能忍耐、小心</dd>
            <dd>可做重复工作（不喜新）不喜展望</dd>
        </dl>
        <dl>
            <dt>直觉（N）：</dt>
            <dd>通过第六感洞察世界、注重应该如何，相对较笼统</dd>
            <dd>喜学新技能</dd>
            <dd>不重准确、喜抽象和理论</dd>
            <dd>重可能性，讨厌细节</dd>
            <dd>好高骛远，喜欢新问题</dd>
            <dd>凭爱好做事，对事情的态度易变</dd>
            <dd>提新见解、匆促结论</dd>
        </dl>
        <h5>思考（T）和情感（F）</h5>
        <dl>
            <dt>思考（T）：</dt>
            <dd>基于分析，用逻辑客观方式决策 </dd>
            <dd>坚信自己的观点正确，不考虑他人意见</dd>
            <dd>清晰、正义、不喜欢调和主义</dd>
            <dd>批判和鉴别力</dd>
            <dd>规则</dd>
            <dd>工作中少表现出情感，也不喜欢他人感情用事</dd>
        </dl>
        <dl>
            <dt>情感（F）：</dt>
            <dd>主观和综合，用个人化的、价值导向的方式决策</dd>
            <dd>考虑决策对他人的影响</dd>
            <dd>和谐、宽容、喜欢调解</dd>
            <dd>不按照逻辑思考</dd>
            <dd>注重周围环境</dd>
            <dd>喜欢工作场景中的情感，从赞美中得到享受，也希望他人的赞美</dd>
        </dl>
        <h5>判断（J）和知觉（P）</h5>
        <dl>
            <dt>判断（J）：</dt>
            <dd>封闭定向封闭定向</dd>
            <dd>结构化和组织化</dd>
            <dd>时间导向</dd>
            <dd>决断，事情都有正误之分</dd>
            <dd>喜命令，控制、反应迅速，喜欢完成任务</dd>
            <dd>不善适应</dd>
        </dl>
        <dl>
            <dt>知觉（P）:</dt>
            <dd>开放定向</dd>
            <dd>弹性化和自发化</dd>
            <dd>探索和开放结局</dd>
            <dd>好奇，喜欢收集新信息而不是做结论</dd>
            <dd>喜欢观望，喜欢开始许多新的项目，但不完成</dd>
            <dd>优柔寡断、易分散注意</dd>
        </dl>
